import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const skillsRoot = path.join(projectRoot, "skills");
const outputPath = path.join(projectRoot, "docs", "skills.md");

const SECTION_ORDER = ["shared", "service", "helper", "recipe", "other"];

const SECTION_META = {
  shared: {
    title: "Shared",
    description: "공통 인증, 출력, 안전 규칙을 설명하는 기본 skill."
  },
  service: {
    title: "Services",
    description: "서비스 단위의 기본 skill."
  },
  helper: {
    title: "Helpers",
    description: "자주 쓰는 흐름을 단축하는 helper skill."
  },
  recipe: {
    title: "Recipes",
    description: "여러 단계를 묶은 실전 recipe."
  },
  other: {
    title: "Other",
    description: "분류가 지정되지 않은 skill."
  }
};

function cleanYamlScalar(value) {
  return value.replace(/^['"]|['"]$/g, "").trim();
}

function extractFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) {
    return null;
  }

  return match[1];
}

function extractField(frontmatter, fieldName) {
  const regex = new RegExp(`(?:^|\\n)${fieldName}:\\s*(.+)`);
  const match = frontmatter.match(regex);
  return match ? cleanYamlScalar(match[1]) : undefined;
}

async function loadSkillEntries() {
  const directoryEntries = await fs.readdir(skillsRoot, { withFileTypes: true });
  const skills = [];

  for (const entry of directoryEntries) {
    if (!entry.isDirectory()) {
      continue;
    }

    const skillDir = path.join(skillsRoot, entry.name);
    const skillFile = path.join(skillDir, "SKILL.md");
    const content = await fs.readFile(skillFile, "utf8");
    const frontmatter = extractFrontmatter(content);

    if (!frontmatter) {
      throw new Error(`Frontmatter not found: ${skillFile}`);
    }

    const name = extractField(frontmatter, "name") ?? entry.name;
    const description = extractField(frontmatter, "description") ?? "";
    const category = extractField(frontmatter, "category")?.toLowerCase() ?? "other";

    skills.push({
      name,
      description,
      category: SECTION_META[category] ? category : "other",
      relativePath: `../skills/${entry.name}/SKILL.md`
    });
  }

  return skills.sort((a, b) => a.name.localeCompare(b.name));
}

function buildSection(category, skills) {
  const meta = SECTION_META[category];
  const lines = [
    `## ${meta.title}`,
    meta.description,
    "",
    "| Skill | Description |",
    "|-------|-------------|"
  ];

  for (const skill of skills) {
    lines.push(`| [${skill.name}](${skill.relativePath}) | ${skill.description} |`);
  }

  lines.push("");
  return lines.join("\n");
}

function buildDocument(skills) {
  const lines = [
    "# Skills Index",
    "> Auto-generated by `npm run docs:skills`. Do not edit manually.",
    ""
  ];

  for (const category of SECTION_ORDER) {
    const grouped = skills.filter((skill) => skill.category === category);
    if (grouped.length === 0) {
      continue;
    }

    lines.push(buildSection(category, grouped));
  }

  return `${lines.join("\n")}\n`;
}

async function main() {
  const skills = await loadSkillEntries();
  const output = buildDocument(skills);

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, output, "utf8");
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
